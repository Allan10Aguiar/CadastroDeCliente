    private void tabelaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaClienteMouseClicked
        txtId.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(), 0).toString());
        txtNome.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(), 1).toString());
        txtCpf.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(), 2).toString());

        String sexo = tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(), 3).toString();

        if(sexo.equals("MASCULINO")){
            cbSexo.setSelectedIndex(1);
        }
        if(sexo.equals("FEMININO")){
            cbSexo.setSelectedIndex(2);
        }

    }//GEN-LAST:event_tabelaClienteMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            Pessoa obj = new Pessoa();

            obj.setId(Integer.parseInt(txtId.getText()));

            ClienteDAO dao = new ClienteDAO();
            dao.excluir(obj);
            JOptionPane.showMessageDialog(null, "Excluido com sucesso!");

            txtNome.setText(null);
            txtId.setText(null);
            txtCpf.setText(null);
            cbSexo.setSelectedIndex(0);
            Listar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Aconteceu o erro: " + e);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        try{
            Pessoa cliente = new Pessoa();

            if(txtNome.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Você precisa informar o NOME!", "AVISO!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(txtCpf.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Você precisa informar o CPF!", "AVISO!", JOptionPane.WARNING_MESSAGE);
                return;
            }

            cliente.setNome(txtNome.getText());
            cliente.setCpf(txtCpf.getText());

            if(cbSexo.getSelectedItem().equals("SELECIONE O SEXO")){
                JOptionPane.showMessageDialog(null, "Você precisa selecionar o sexo!", "AVISO!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            else if(cbSexo.getSelectedItem().equals("MASCULINO")){
                cliente.setSexo("MASCULINO");
            }else if(cbSexo.getSelectedItem().equals("FEMININO")){
                cliente.setSexo("FEMININO");
            }

            ClienteDAO obj = new ClienteDAO();

            obj.cadastrar(cliente);

            txtNome.setText(null);
            cbSexo.setSelectedIndex(0);
            txtCpf.setText(null);
            Listar();

            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Aconteceu o erro: " + e);
        }

    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            Pessoa obj = new Pessoa();

            obj.setNome(txtNome.getText());
            obj.setCpf(txtCpf.getText());

            if(cbSexo.getSelectedItem().equals("MASCULINO")){

                obj.setSexo("MASCULINO");

            }else if(cbSexo.getSelectedItem().equals("FEMININO")){

                obj.setSexo("FEMININO");

            }

            obj.setId(Integer.parseInt(txtId.getText()));

            ClienteDAO dao = new ClienteDAO();
            dao.alterar(obj);
            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");

            txtNome.setText(null);
            txtId.setText(null);
            txtCpf.setText(null);
            cbSexo.setSelectedIndex(0);
            Listar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Aconteceu o erro: " + e);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

