    private void btnBuscarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProdutoActionPerformed
        try {
            ProdutoDAO daoP = new ProdutoDAO();
            Produto objP = new Produto();
            
            objP = daoP.buscarDados(Integer.parseInt(txtCodProduto.getText()));
            
            txtNomeProduto.setText(objP.getNomeProduto());
            txtPreco.setText(String.valueOf(objP.getVenda()));
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnBuscarProdutoActionPerformed

    private void btnAddProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProdutoActionPerformed
        try {
            
            ItensVendaDAO daoItem = new ItensVendaDAO();
            ItensVenda objItem = new ItensVenda();
            
            objItem.setIdProdutoFK(Integer.parseInt(txtCodProduto.getText()));
            objItem.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            
            daoItem.inserirItem(objItem);
            
           
            int qtd = Integer.parseInt(txtQuantidade.getText());
            double preco = Double.parseDouble(txtPreco.getText());
            sub = qtd*preco;            
            
            total += sub;
            
            txtSubTotal.setText(""+ sub);
            txtTotal.setText(""+total);
            
            Listar();
            limparCampos();
            txtCodProduto.requestFocus();
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAddProdutoActionPerformed

