    public void Listar() {
        try {
                ItensVendaDAO dao = new ItensVendaDAO();
                List<ItensVenda> lista = dao.listarTodosItens();
                DefaultTableModel model = (DefaultTableModel) tbItensAdd.getModel();
                model.setNumRows(0);

            for (ItensVenda obj : lista) {
                model.addRow(new Object[]{
                    obj.getIdItemVenda(),
                    obj.getNomeProduto(),
                    obj.getQuantidade(),
                    obj.getValorUnit(),
                    obj.getValorItem()
                    
                });
            }
        } catch (Exception e) {
            System.out.println("Aconteceu o erro: " + e);
        }
    }

