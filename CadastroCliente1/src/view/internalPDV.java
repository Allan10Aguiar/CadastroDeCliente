    private void btnRemoveProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveProdutoActionPerformed
        try {
            ItensVenda obj = new ItensVenda();
            ItensVendaDAO dao = new ItensVendaDAO();
            
            obj.setIdItemVenda(cod);
            
            dao.excluir(obj);
            
            total -= valItem;
            sub = 0;
            
            txtSubTotal.setText(""+ sub);
            txtTotal.setText(""+total);
            
            Listar();
            limparCampos();
            txtCodProduto.requestFocus();
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnRemoveProdutoActionPerformed

