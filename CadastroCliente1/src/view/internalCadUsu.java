    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            Usuario obj = new Usuario();

            obj.setIdUsuario(Integer.parseInt(txtId.getText()));

            UsuarioDAO dao = new UsuarioDAO();
            dao.excluir(obj);
            JOptionPane.showMessageDialog(null, "Excluido com sucesso!");

            txtNomeUsuario.setText(null);
            txtId.setText(null);
            txtSenha.setText(null);
            Listar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Aconteceu o erro: " + e);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        try{
            Usuario obj = new Usuario();

            if(txtNomeUsuario.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Voc√™ precisa informar o NOME!", "AVISO!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(txtSenha.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Voc√™ precisa informar a SENHA!", "AVISO!", JOptionPane.WARNING_MESSAGE);
                return;
            }

            obj.setNomeDeUsuario(txtNomeUsuario.getText());
            obj.setSenhaDoUsuario(txtSenha.getText());

            UsuarioDAO dao = new UsuarioDAO();

            dao.cadastrarUsuario(obj);

            txtNomeUsuario.setText(null);
            txtSenha.setText(null);
            Listar();

            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Aconteceu o erro: " + e);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            Usuario obj = new Usuario();

            obj.setNomeDeUsuario(txtNomeUsuario.getText());
            obj.setSenhaDoUsuario(txtSenha.getText());

            obj.setIdUsuario(Integer.parseInt(txtId.getText()));

            UsuarioDAO dao = new UsuarioDAO();
            dao.alterar(obj);
            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");

            txtNomeUsuario.setText(null);
            txtId.setText(null);
            txtSenha.setText(null);
            Listar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Aconteceu o erro: " + e);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

